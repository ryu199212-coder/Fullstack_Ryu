package com.company.java002_ex;

public class Var002 {
     public static void main(String[] args) {
     int a=10;
     int b=3;
     float c;
     c=(float)a/b;
     System.out.println(a+"+"+b+"="+(a+b));
     System.out.println(a+"-"+b+"="+(a-b));
     System.out.println(a+"*"+b+"="+(a*b));
     System.out.println(a+"/"+b+"="+(a/b));
     
     
     System.out.printf("%d+%d=%f", a, b,c);
     System.out.printf("%d-%d=%f", a, b,c);
     System.out.printf("%d*%d=%f", a, b,c);	
     System.out.printf("%d/%d=%f", a, b,c);
     
     
     
     }
}